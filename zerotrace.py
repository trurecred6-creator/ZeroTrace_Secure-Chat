"""# Encrypt AES Key using RSA"""

def rsa_encrypt_key(aes_key, public_key):

    encrypted = rsa.encrypt(aes_key, public_key)

    encoded = base64.b64encode(encrypted).decode()

    print("AES Key Encrypted using RSA")

    return encoded

"""# Decrypt AES Key"""

def rsa_decrypt_key(encrypted_key, private_key):

    decoded = base64.b64decode(encrypted_key)

    key = rsa.decrypt(decoded, private_key)

    print("AES Key Decrypted using RSA")

    return key

"""# Secret Sharing (Novel Group Security)"""

def generate_group_key():

    key = secrets.randbits(128)

    print("Group Key Generated:", key)

    return key


def split_group_key(group_key, members=5):

    shares = []

    for i in range(members-1):
        shares.append(secrets.randbits(128))

    last = group_key

    for share in shares:
        last ^= share

    shares.append(last)

    print("Group Key Split into Shares")

    return shares


def reconstruct_group_key(shares):

    key = 0

    for share in shares:
        key ^= share

    print("Group Key Reconstructed")

    return key

