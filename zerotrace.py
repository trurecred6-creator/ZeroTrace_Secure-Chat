"""# AES Encryption"""

def aes_encrypt(message, key):

    iv = os.urandom(16)

    padder = PKCS7(128).padder()
    padded = padder.update(message.encode()) + padder.finalize()

    cipher = Cipher(
        algorithms.AES(key),
        modes.CBC(iv),
        backend=default_backend()
    )

    encryptor = cipher.encryptor()

    ciphertext = encryptor.update(padded) + encryptor.finalize()

    encrypted = base64.b64encode(iv + ciphertext).decode()

    print("Message Encrypted using AES-256")

    return encrypted

"""# AES Decryption"""

def aes_decrypt(ciphertext, key):

    data = base64.b64decode(ciphertext)

    iv = data[:16]
    encrypted = data[16:]

    cipher = Cipher(
        algorithms.AES(key),
        modes.CBC(iv),
        backend=default_backend()
    )

    decryptor = cipher.decryptor()

    padded = decryptor.update(encrypted) + decryptor.finalize()

    unpadder = PKCS7(128).unpadder()

    message = unpadder.update(padded) + unpadder.finalize()

    print("Message Decrypted using AES-256")

    return message.decode()

"""# SHA-256 Integrity (Novel Tamper Detection)"""

def generate_hash(message):

    hash_value = hashlib.sha256(message.encode()).hexdigest()

